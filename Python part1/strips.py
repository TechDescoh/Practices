#We set the value of the variable 
message_space = " python "
print(message_space)
#we put the value with not right strip
message_space = message_space.rstrip()
print(message_space)
#we put the value with not left strip
message_space = message_space.lstrip()
print(message_space)
#we put the value with not strips
message_space = message_space.strip()
print(message_space)